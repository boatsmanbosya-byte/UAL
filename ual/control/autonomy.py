"""
Динамика α_t, K_t, C(K_t) из формальной модели (дневник, разделы 4-6),
теперь с константами, откалиброванными под РЕАЛЬНЫЕ масштабы d,
измеренные distance.py (единичное AUTO_ACCEPT правило ~3.5, батч ~24.5),
а не под условные 0.1-0.5 из первой численной симуляции.

ЧЕСТНО: C0/Cmax/Kscale/gamma/lam — по-прежнему дизайнерские константы,
подобранные так, чтобы демонстрация была наглядной на реальных числах.
Они НЕ калиброваны на настоящем операторе-человеке — это остаётся
открытым научным долгом, как и все веса в этом проекте.
"""
import math


def sigma(V: float, s: float = 5.0) -> float:
    return math.tanh(V / s)


def comprehension_capacity(K: float, C0: float = 10.0, Cmax: float = 20.0, Kscale: float = 15.0) -> float:
    """C(K) — насыщающаяся пропускная способность понимания оператора.
    C0=10.0: с самого начала (K=0) оператор способен переварить d~10 за шаг —
    подобрано так, чтобы совпасть с иллюстративными числами из обсуждения
    (C(K_t)=10.0 / 15.0), а не как измеренная величина."""
    return C0 + Cmax * (1 - math.exp(-K / Kscale))


def update_K(K: float, d_digested: float, gamma: float = 0.4) -> float:
    """Рост понимания от переваренного (успешно принятого) эффективного сдвига,
    с убывающей отдачей по мере роста K (аналог diminishing returns)."""
    return K + gamma * d_digested / (1 + 0.3 * K)


def update_alpha(alpha: float, V: float, lam: float = 0.06, s: float = 5.0) -> float:
    """α_{t+1} = clip(α_t + λ·σ(V), 0, 1) — без асимметрии рост/откат,
    как договаривались (гибкость и плавность важнее жёсткого тормоза)."""
    return max(0.0, min(1.0, alpha + lam * sigma(V, s)))
